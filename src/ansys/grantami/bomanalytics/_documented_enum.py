from enum import Enum
from typing import Any


class _DocumentedEnum(Enum):
    """Base class a documented enum.

    Overrides `__new__` to populate the __doc__ on an enum member.
    """

    def __new__(cls, value: Any, doc: str) -> "_DocumentedEnum":
        obj: _DocumentedEnum = object.__new__(cls)
        obj._value_ = value
        obj.__doc__ = " ".join(doc.split())
        return obj
